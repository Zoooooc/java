package org.opentutorials.javatutorials.variables;

public class DoubleDemo {

	public static void main(String[] args) {
		double a = 1.1;
		System.out.println(a);
		
		// 정수형 데이터 타입(자료형)
		// float   4byte  
		// double  8byte  
		
		float b = 2.2f;
		System.out.println(b);
		
		// 자동 형 변환
		double c = 3.0f;
		
		// 자동 형 변환이 일어나는 규칙
		// byte > short > int > long > float > double
		//         char > int
		
	}
}
